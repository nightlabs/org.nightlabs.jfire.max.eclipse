package org.nightlabs.jfire.reporting.ui.viewer.editor;

import org.eclipse.core.runtime.IProgressMonitor;
import org.eclipse.ui.IEditorInput;
import org.eclipse.ui.IEditorSite;
import org.eclipse.ui.PartInitException;
import org.eclipse.ui.part.EditorPart;

/**
 * Abstract implementation of an Editor that can be used as
 * {@link ReportViewerEditor} it provides several helper
 * methods that can be used by concrete implementations in order to
 * show reports.
 * 
 * @author Alexander Bieber <alex[AT]nightlabs[DOT]de>
 *
 */
public abstract class AbstractReportViewerEditor
extends EditorPart
implements ReportViewerEditor
{
	
	private ReportViewerEditorInput viewerInput;
	
	/**
	 * 
	 */
	public AbstractReportViewerEditor() {
		super();
	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.part.EditorPart#doSave(org.eclipse.core.runtime.IProgressMonitor)
	 */
	@Override
	public void doSave(IProgressMonitor monitor) {
	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.part.EditorPart#doSaveAs()
	 */
	@Override
	public void doSaveAs() {
	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.part.EditorPart#init(org.eclipse.ui.IEditorSite, org.eclipse.ui.IEditorInput)
	 */
	@Override
	public void init(IEditorSite site, IEditorInput input)
			throws PartInitException {
		setSite(site);
		setInput(input);
		if (input instanceof ReportViewerEditorInput)
			viewerInput = (ReportViewerEditorInput)input;
	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.part.EditorPart#isDirty()
	 */
	@Override
	public boolean isDirty() {
		return false;
	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.part.EditorPart#isSaveAsAllowed()
	 */
	@Override
	public boolean isSaveAsAllowed() {
		return false;
	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.part.WorkbenchPart#setFocus()
	 */
	@Override
	public void setFocus() {
	}


	/**
	 * Returns the {@link ReportViewerEditorInput} associated with this editor.
	 * @return The {@link ReportViewerEditorInput} associated with this editor.
	 */
	public ReportViewerEditorInput getViewerInput() {
		return viewerInput;
	}
}

