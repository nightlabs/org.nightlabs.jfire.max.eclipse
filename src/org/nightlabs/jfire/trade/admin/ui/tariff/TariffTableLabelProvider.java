package org.nightlabs.jfire.trade.admin.ui.tariff;

import org.eclipse.jface.viewers.ITableLabelProvider;
import org.eclipse.jface.viewers.LabelProvider;
import org.eclipse.swt.graphics.Image;

/**
 * @author Marco Schulze - marco at nightlabs dot de
 * @deprecated not used anywhere - Daniel
 */
@Deprecated
public class TariffTableLabelProvider
	extends LabelProvider
	implements ITableLabelProvider
{
	private TariffListComposite tariffListComposite;

	public TariffTableLabelProvider(TariffListComposite tariffListComposite)
	{
		this.tariffListComposite = tariffListComposite;
	}

	/**
	 * @see org.eclipse.jface.viewers.ITableLabelProvider#getColumnImage(java.lang.Object, int)
	 */
	public Image getColumnImage(Object element, int columnIndex)
	{
		return null;
	}

	/**
	 * @see org.eclipse.jface.viewers.ITableLabelProvider#getColumnText(java.lang.Object, int)
	 */
	public String getColumnText(Object element, int columnIndex)
	{
		String res = null;
		if (element instanceof TariffCarrier) {
			TariffCarrier tc = (TariffCarrier)element;
			switch(columnIndex) {
				case 0:
					res = tc.getTariff().getName().getText(tariffListComposite.getLanguageID());
				break;
			}
		}
		if (res != null)
			return res;
		else
			return ""; //$NON-NLS-1$
	}

}
